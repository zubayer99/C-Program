#include <stdio.h>
int main()
{
    double u, a, t , v, s;


    printf("Enter velocity : ");
    scanf("%lf", &v);

    printf("Enter time : ");
    scanf("%lf", &t);

    a = a;
    u = v - a * t;


    s = u * t + (0.5 * a * (t*t));

    printf("S = %d", s);

    return 0;


}
